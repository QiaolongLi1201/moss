#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const defaultPackageDir = path.resolve(scriptDir, '..');

function argValue(name) {
  const index = process.argv.indexOf(name);
  return index >= 0 ? process.argv[index + 1] : undefined;
}

const packageDir = path.resolve(argValue('--package-dir') || defaultPackageDir);
const zeroConfigPath = path.join(packageDir, 'zero-config-default.json');
const markerPath = path.join(packageDir, '.zero-config-default.generated');

function fail(message) {
  console.error(`[zero-config] ${message}`);
  process.exit(1);
}

function readJsonFile(file) {
  return JSON.parse(fs.readFileSync(file, 'utf8'));
}

function validateConfig(config, source) {
  if (!config || typeof config !== 'object' || Array.isArray(config)) {
    fail(`${source}: expected a JSON object`);
  }
  const required = ['provider', 'model', 'baseUrl', 'apiKey'];
  for (const key of required) {
    if (typeof config[key] !== 'string' || !config[key].trim()) {
      fail(`${source}: ${key} must be a non-empty string`);
    }
  }
  try {
    const url = new URL(config.baseUrl);
    if (url.protocol !== 'http:' && url.protocol !== 'https:') {
      fail(`${source}: baseUrl must use http:// or https://`);
    }
  } catch {
    fail(`${source}: baseUrl must be a valid URL`);
  }
  return {
    provider: config.provider,
    model: config.model,
    baseUrl: config.baseUrl,
    apiKey: config.apiKey,
  };
}

function configFromEnv() {
  const file = process.env.DMOSS_ZERO_CONFIG_DEFAULT_FILE || process.env.DMOSS_BUNDLED_DEFAULT_FILE;
  if (file) return validateConfig(readJsonFile(file), file);

  const json = process.env.DMOSS_ZERO_CONFIG_DEFAULT_JSON;
  if (json) return validateConfig(JSON.parse(json), 'DMOSS_ZERO_CONFIG_DEFAULT_JSON');

  const config = {
    provider: process.env.DMOSS_ZERO_CONFIG_PROVIDER,
    model: process.env.DMOSS_ZERO_CONFIG_MODEL,
    baseUrl: process.env.DMOSS_ZERO_CONFIG_BASE_URL,
    apiKey: process.env.DMOSS_ZERO_CONFIG_API_KEY,
  };
  if (Object.values(config).some(Boolean)) {
    return validateConfig(config, 'DMOSS_ZERO_CONFIG_*');
  }
  return null;
}

if (fs.existsSync(zeroConfigPath)) {
  validateConfig(readJsonFile(zeroConfigPath), zeroConfigPath);
  console.error(`[zero-config] using existing ${path.relative(packageDir, zeroConfigPath)}`);
  process.exit(0);
}

const config = configFromEnv();
if (!config) {
  fail(
    'missing zero-config-default.json. Set DMOSS_ZERO_CONFIG_DEFAULT_FILE, ' +
    'DMOSS_ZERO_CONFIG_DEFAULT_JSON, or DMOSS_ZERO_CONFIG_PROVIDER/MODEL/BASE_URL/API_KEY before packing @rdk-moss/agent.',
  );
}

fs.writeFileSync(zeroConfigPath, `${JSON.stringify(config, null, 2)}\n`, { mode: 0o600 });
fs.writeFileSync(markerPath, 'generated by prepare-zero-config-default.mjs\n', { mode: 0o600 });
console.error(`[zero-config] generated ${path.relative(packageDir, zeroConfigPath)} for package packing`);
